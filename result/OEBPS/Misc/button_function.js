function Adressen() {
          var elms = document.querySelectorAll("#myDIV");
          
          for (var i = 0; i < elms.length; i++) {

            if (elms[i].style.display === "none") {
              elms[i].style.display = "block";
            } else {
              elms[i].style.display = "none";
            }
          }
        }
